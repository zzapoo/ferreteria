    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        AddHerramienta ah= new AddHerramienta(this, true);
        ah.setVisible(true);
        Herramienta h = new Herramienta(f.getRazonSocial(),ah.getNombre(), ah.getMarca(), ah.getModelo(), ah.getTamaño(), ah.getPrecio(), ah.getExistencias());
        if(ah.isValido()){
            f.altaHerramienta(h);
            operacionesSQL.instancia().addH(h);
        }
    }//GEN-LAST:event_addActionPerformed

    private void delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delActionPerformed
        DelHerramienta dh = new DelHerramienta(this, true);
        dh.setVisible(true);
        if (dh.isValido()){
            if(f.bajaHerramienta(dh.getMarca(), dh.getModelo())){
                JOptionPane.showMessageDialog(this, "Herramienta eliminada");
            }else{
                JOptionPane.showMessageDialog(this, "La herramienta no existe");
            }
            
        }
    }//GEN-LAST:event_delActionPerformed

    private void modActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modActionPerformed
        ModHerramienta mh = new ModHerramienta(this, true,f);
        mh.setVisible(true);
        if (mh.isValido() && mh.getjButton4().isEnabled()){
            f.modificarExistencias(mh.getMarca(), mh.getModelo(), mh.getExistencias());
        } else if (mh.isValido() && mh.getjButton5().isEnabled()){
            f.modificarPrecio(mh.getMarca(), mh.getModelo(), mh.getPrecio());
        }
    }//GEN-LAST:event_modActionPerformed

