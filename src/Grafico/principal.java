    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        LeerFerreteria lf = new LeerFerreteria(this, true);
        lf.setVisible(true);
                
        if(lf.isValido()){
            f = new Ferreteria(lf.getRazonSocial(), lf.getDireccion(), lf.getTitular(), lf.getStock());
            jPanel1.setEnabled(true);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        NuevaFerreteria nf = new NuevaFerreteria(this, true);
        nf.setVisible(true);
        
        if(nf.isValido()){
            f = new Ferreteria(nf.getRazonSocial(), nf.getDireccion(), nf.getTitular(), 500);
            try {
                operacionesSQL.instancia().crearF(f);
            } catch (ErrorCreacionFerreteria ex) {
                Logger.getLogger(principal.class.getName()).log(Level.SEVERE, null, ex);
            }
            jPanel1.setEnabled(true);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

